export function mostraIdade(Nome, Idade) {
  let nome = Nome;
  let idade = Idade;

  return `A idade de ${nome} é ${idade}`;
}

export function mostraCidade(Cidade, habitantes) {
  let cidade = Cidade;
  let QtdPessoas = habitantes;

  return `Moro na cidade de ${cidade} ela já tem ${QtdPessoas} habitantes`;
}
export function mostraHobby(Hobby, Anos) {
  let hobbie = Hobby;
  let anos = Anos;

  return `Meu Hobby é ${hobbie} e já faço ele a ${anos} anos`;
}
